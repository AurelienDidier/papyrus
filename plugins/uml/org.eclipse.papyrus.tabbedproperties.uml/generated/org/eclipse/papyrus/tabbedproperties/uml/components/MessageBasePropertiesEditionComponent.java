	/**
	 * {@inheritDoc}
	 * 
	 * @see org.eclipse.emf.eef.runtime.api.component.IPropertiesEditionComponent#getPropertiesEditionObject()
	 */
	public EObject getPropertiesEditionObject(EObject source) {
		if (source instanceof Message) {
			Message messageToUpdate = (Message) source;
			messageToUpdate.getOwnedComments().addAll(basePart.getOwnedCommentToAdd());
			messageToUpdate.setName(basePart.getName());

			messageToUpdate.setVisibility((VisibilityKind) basePart.getVisibility());

			messageToUpdate.getClientDependencies().addAll(basePart.getClientDependencyToAdd());
			messageToUpdate.setMessageSort((MessageSort) basePart.getMessageSort());

			messageToUpdate.getArguments().addAll(basePart.getArgumentToAdd());

			return messageToUpdate;
		} else
			return null;
	}

