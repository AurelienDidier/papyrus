	/**
	 * {@inheritDoc}
	 * 
	 * @see org.eclipse.emf.eef.runtime.api.component.IPropertiesEditionComponent#getPropertiesEditionPart (java.lang.String, java.lang.String)
	 */
	public IPropertiesEditionPart getPropertiesEditionPart(int kind, String key) {
		if (message != null && BASE_PART.equals(key)) {
			if (basePart == null) {
				IPropertiesEditionPartProvider provider = PropertiesEditionPartProviderService.getInstance().getProvider(UMLViewsRepository.class);
				if (provider != null) {
					basePart = (MessagePropertiesEditionPart) provider.getPropertiesEditionPart(UMLViewsRepository.Message.class, kind, this);
					listeners.add(basePart);
				}
			}
			return (IPropertiesEditionPart) basePart;
		}
		return null;
	}

	/**
	 * {@inheritDoc}
	 * 
	 * @see org.eclipse.emf.eef.runtime.api.component.IPropertiesEditionComponent#initPart(java.lang.Class, int, org.eclipse.emf.ecore.EObject, org.eclipse.emf.ecore.resource.ResourceSet)
	 */
	public void initPart(java.lang.Class key, int kind, EObject elt, ResourceSet allResource) {
		if (basePart != null && key == UMLViewsRepository.Message.class) {
			((IPropertiesEditionPart) basePart).setContext(elt, allResource);
			Message message = (Message) elt;
			basePart.initOwnedComment(message, null, UMLPackage.eINSTANCE.getElement_OwnedComment());
			if (message.getName() != null)
				basePart.setName(message.getName());

			basePart.initVisibility((EEnum) UMLPackage.eINSTANCE.getNamedElement_Visibility().getEType(), message.getVisibility());
			basePart.initClientDependency(message, null, UMLPackage.eINSTANCE.getNamedElement_ClientDependency());
			basePart.initMessageSort((EEnum) UMLPackage.eINSTANCE.getMessage_MessageSort().getEType(), message.getMessageSort());
			basePart.initArgument(message, null, UMLPackage.eINSTANCE.getMessage_Argument());
		}

	}

